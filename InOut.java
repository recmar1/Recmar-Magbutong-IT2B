    public void loadtheTable() {
        String filePath = "C:\\Textfile\\textfile.txt";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            DefaultTableModel model = (DefaultTableModel) content.getModel();
            
            
            String line;
            while ((line = br.readLine()) != null) {
            // Only import rows that meet a certain condition (e.g., containing a specific keyword)
            if (line.contains("Input/Output Device")) {
            String[] row = line.split(" , ");
            model.addRow(row);
        }
    }
            
        } catch (IOException ex) {
            Logger.getLogger(Input.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
